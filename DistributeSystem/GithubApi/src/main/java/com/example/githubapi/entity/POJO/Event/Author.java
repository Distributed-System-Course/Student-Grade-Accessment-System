package com.example.githubapi.entity.POJO.Event;

public class Author {
    String email;
    String name;
    public void setEmail(String email){
        this.email=email;
    }
    public String getEmail(){
        return email;
    }
    public void setName(String name){
        this.name=name;
    }
    public String getName(){
        return name;
    }
}
